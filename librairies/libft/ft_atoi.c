#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	resultat;
	int	sign;
	int	i;

	sign = 1;
	resultat = 0;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-')
	{
		sign *= -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		resultat = resultat * 10 + (nptr[i] - '0');
		i++;
	}
	return (resultat * sign);
}
