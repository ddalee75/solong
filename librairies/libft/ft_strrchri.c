#include "libft.h"

int	ft_strrchri(const char *s, int c)
{	
	int	i;
	
	i = 0;
	while (s[i])
		i++;

	while (i >= 0)
	{
		if (s[i] == (char)c)
			return (i);
		i--;
	}
	return (0);
}
